#include "libft.h"

/*
**	ft_strclr :
**	-> replace char pointed by s with '\0'
*/

void	ft_strclr(char *s)
{
	size_t i;

	i = 0;
	if (s)
	{
		if (s[i] != '\0')
		{
			while (s[i])
				s[i++] = '\0';
		}
	}
}
